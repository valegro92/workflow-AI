/**
 * Ottieni template per categoria
 */
export function getTemplatesByCategory(categoria: string): WorkflowTemplate[] {
  return workflowTemplates.filter((t) => t.categoria === categoria);
}

/**
 * Ottieni tutti i template
 */
export function getAllTemplates(): WorkflowTemplate[] {
  return workflowTemplates;
}

/**
 * Cerca template per keyword
 */
export function searchTemplates(query: string): WorkflowTemplate[] {
  const lowerQuery = query.toLowerCase();
  return workflowTemplates.filter(
    (t) =>
      t.titolo.toLowerCase().includes(lowerQuery) ||
      t.descrizione.toLowerCase().includes(lowerQuery) ||
      t.workflow.descrizione.toLowerCase().includes(lowerQuery)
  );
}
